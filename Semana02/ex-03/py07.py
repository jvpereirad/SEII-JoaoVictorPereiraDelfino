# Operadores de comparação
a = 10
b = 5

igual = a == b
diferente = a != b
maior = a > b
menor = a < b
maior_ou_igual = a >= b
menor_ou_igual = a <= b

print("Igual:", igual)
print("Diferente:", diferente)
print("Maior:", maior)
print("Menor:", menor)
print("Maior ou Igual:", maior_ou_igual)
print("Menor ou Igual:", menor_ou_igual)
